package ViDu_Chuong_3;
// các ký tự có chức năng đặc biệt trong java
/*
\n : cho phép xuống dòng
\t : tạo một tab ngang ( 4 khoảng space)
\b : xóa bỏ một ký tự từ cuối dòng
\\ : tạo một dấu ngang
\" : tạo một dấu ngoặc kép
\' : tạo một dấu ngoặc đơn
 */
public class bai7_video {
    public static void main(String[] args) {
        System.out.println("Java\nTutorial\nn07");
        System.out.println("Java\tTutorial\tn07");
        System.out.println("Java Tutorial n07\b");
        System.out.println("Java\\Tutorial\\n07");
        System.out.println("\"Java Tutorial n07\"");
        System.out.println("\'Java Tutorial n07\'");
    }
}
