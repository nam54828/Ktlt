package ViDu_Chuong_3;
// cấu trúc điều khiển lặp do-while
public class bai26_video {
    public static void main(String[] args) {
        /*
        do{
        làm gì đó;
        }while ( điều kiện lặp)
         */
        int i=0;
        while(i !=0){
            System.out.println("Hello");
        }
        do{
            System.out.println("Hello");
        }while(i != 0);
    }
}
